function latdir($lat) {
  if ($lat > 0) {
    return "N";
  } elseif ($lat < 0) {
    return "S";
  } else {
    return "";
  }
}

function londir($lon) {
  if ($lon > 0) {
    return "E";
  } elseif ($lon < 0) {
    return "W";
  } else {
    return "";
  }
